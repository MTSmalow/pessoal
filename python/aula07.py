#testes de tipos
n = input("digite algo ")
print(type(n))
print(n.isalnum)#verifica se e alfabetico(letra) e numerico(numero)
print(n.isnumeric)#verifica se e numero
print(n.isalpha)#verifica se e alphabetico(letra)
print(n.isupper)#verifica se esta em maiusculo
print(n.islower)#verifica se esta em minusculo
print(n.isdecimal)#verifica se e deciimal
print(n.isprintable)#verifica se pode ser inpresso
print(n.isspace)# verifica se tem espa√ßo
print(n.istitle)#verifica se esta capitalzada